function getIdLocation() {
    var id = req.get('lid');
    var prototypes = req.get('prototypes');
    if (id == null) {
	res.write('');
    } else {
	var objs = app.getObjects((prototypes || []), {_id: id});
	if (objs.length < 1) {
	    res.write('');
	} else {
	    res.write(objs[0].getURI());
	}
    }
}
